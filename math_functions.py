"""
数学函数
"""
# 绝对值
import math

# from math import ceil

print(abs(-40))  # 40

# 向上取整
print(math.ceil(7 / 3))  # 3

# 返回e的x次幂ex
print(math.exp(1))  # 2.718281828459045

# 返回绝对值，float 类型的
print(math.fabs(99))  # 99.0

# 向下取整

print(math.floor(9.12))  # 9

# 对数-浮点类型
print(math.log(math.e))  # 1.0
# 10的对数-浮点类型
print(math.log10(1000))  # 3.0
# 最大值
print(max(99, 3, 36, 6))  # 99
# 最小值
print(min(99, 3, 36, 6))  # 3

